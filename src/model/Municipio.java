package model;

public class Municipio {
    private String codigoMunicipio;
    private String nome;
    private String popTotal;
    private String popUrbana;

    public String getCodigoMunicipio() {
        return codigoMunicipio;
    }

    public void setCodigoMunicipio(String codigoMunicipio) {
        this.codigoMunicipio = codigoMunicipio;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getPopTotal() {
        return popTotal;
    }

    public void setPopTotal(String popTotal) {
        this.popTotal = popTotal;
    }

    public String getPopUrbana() {
        return popUrbana;
    }

    public void setPopUrbana(String popUrbana) {
        this.popUrbana = popUrbana;
    }

   
    
    
    
}
